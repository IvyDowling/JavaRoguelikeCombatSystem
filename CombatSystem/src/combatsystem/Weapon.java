package combatsystem;

public class Weapon {

    private boolean isSharp;
    private int length;
    private int weight;
    private int sharpnessBonus;

    public boolean isIsSharp() {
        return isSharp;
    }

    public int getLength() {
        return length;
    }

    public int getWeight() {
        return weight;
    }

    public int getSharpnessBonus() {
        return sharpnessBonus;
    }
}
